#!/usr/bin/python3
#if/then examples
#the elif funtion statement means else if 
#you can also use just "else" which means anything other than the2 statmnts
def main():
  x=100
  y=100
  if x < y:
    print("x is less than y")
  elif x > y:
    print("x is greater than y")
  else:
    print("x is equal to y")


main()
